class Solution {

/*****
s: integer, starting point of Sam's house location.
t: integer, ending location of Sam's house location.
a: integer, location of the Apple tree.
b: integer, location of the Orange tree.
apples: integer array, distances at which each apple falls from the tree.
oranges: integer array, distances at which each orange falls from the tree.

how many apples and oranges will fall on Sam's house ?
*****/
    // Complete the countApplesAndOranges function below.
    static void countApplesAndOranges(int s, int t, int a, int b, int[] apples, int[] oranges) {
        int apple_fall_on_house = 0;
        foreach(var apple in apples){
            if (apple <0){
                continue;
            }
            int apple_pos = a + apple;
            if (s <= apple_pos && apple_pos <= t){
                apple_fall_on_house++;
            }
        }
        Console.WriteLine(apple_fall_on_house);
        
        int orange_fall_on_house = 0;
        foreach(var orange in oranges){
            if (orange >0){
                continue;
            }
            int orange_pos = b + orange;
            if (s <= orange_pos && orange_pos <= t){
                orange_fall_on_house++;
            }
        } 
        Console.WriteLine(orange_fall_on_house);       
    }

    static void Main(string[] args) {
        string[] st = Console.ReadLine().Split(' ');

        int s = Convert.ToInt32(st[0]);

        int t = Convert.ToInt32(st[1]);

        string[] ab = Console.ReadLine().Split(' ');

        int a = Convert.ToInt32(ab[0]);

        int b = Convert.ToInt32(ab[1]);

        string[] mn = Console.ReadLine().Split(' ');

        int m = Convert.ToInt32(mn[0]);

        int n = Convert.ToInt32(mn[1]);

        int[] apples = Array.ConvertAll(Console.ReadLine().Split(' '), applesTemp => Convert.ToInt32(applesTemp))
        ;

        int[] oranges = Array.ConvertAll(Console.ReadLine().Split(' '), orangesTemp => Convert.ToInt32(orangesTemp))
        ;
        countApplesAndOranges(s, t, a, b, apples, oranges);
    }
}
